function myFunction() {
  var x = document.getElementById("numberOne");
  var y = document.getElementById("firstp");
  var z = document.getElementById("firstPic");
  if (x.style.display === "none" && y.style.fontWeight !== 400 && z.style.transform !== "rotate(180)") {
    x.style.display = "block";
    y.style.fontWeight = 600;
    z.style.transform = "rotate(180deg)";
  } else {
    x.style.display = "none";
    y.style.fontWeight = 400;
    z.style.transform = "rotate(0)";
  }
}

